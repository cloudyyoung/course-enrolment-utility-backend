<h2>What are cookies? // 什么是 Cookies？ // 什麼是 Cookies？</h2>

<p>
	<span>Cookies are small pieces of data stored in text files that are saved on your computer or other devices when
		websites are loaded in a browser. They are widely used to remember you and your preferences, either for a single
		visit (through a &quot;session cookie&quot;) or for multiple repeat visits (using a &quot;persistent
		cookie&quot;).</span>
	<span>Cookie是存储在文本文件中的小块数据，当您在浏览器中加载网站时，这些数据会保存在您的计算机或其他设备上。它们被广泛用于记住您和您的偏好，无论是单次访问（通过“会话cookie”）还是多次重复访问（使用“持久性cookie”）。</span>
	<span>Cookie是存儲在文本文件中的小塊數據，當您在瀏覽器中加載網站時，這些數據會保存在您的計算機或其他設備上。它們被廣泛用於記住您和您的偏好，無論是單次訪問（通過“會話cookie”）還是多次重複訪問（使用“持久性cookie”）。</span>
</p>

<p>
	<span>Session cookies are temporary cookies that are used during the course of your visit to the Website, and they
		expire when you close the web browser.</span>
	<span>会话cookie是在您访问本网站期间使用的临时cookie，它们在您关闭Web浏览器时会过期。</span>
	<span>會話cookie是在您訪問本網站期間使用的臨時cookie，它們在您關閉Web瀏覽器時會過期。</span>
</p>

<p>
	<span>Persistent cookies are used to remember your preferences within our Website and remain on your desktop or
		mobile device even after you close your browser or restart your computer. They ensure a consistent and efficient
		experience for you while visiting our Website or using our Services.</span>
	<span>永久性Cookie用于记住您在我们网站中的偏好，即使在您关闭浏览器或重新启动计算机后仍保留在桌面或移动设备上。他们在访问我们的网站或使用我们的服务时确保为您提供一致和高效的体验。</span>
	<span>永久性Cookie用於記住您在我們網站中的偏好，即使在您關閉瀏覽器或重新啟動計算機後仍保留在桌面或移動設備上。他們在訪問我們的網站或使用我們的服務時確保為您提供一致和高效的體驗。</span>
</p>

<p>
	<span>Cookies may be set by the Website (&quot;first-party cookies&quot;), or by third parties, such as those who
		serve content or provide advertising or analytics services on the website (&quot;third party cookies&quot;).
		These third parties can recognize you when you visit our website and also when you visit certain other
		websites.</span>
	<span>Cookie可以由网站（“第一方Cookie”）或第三方设置，例如在网站上提供内容或提供广告或分析服务的人（“第三方Cookie”）。当您访问我们的网站时以及访问某些其他网站时，这些第三方可以识别您。</span>
	<span>Cookie可以由網站（“第一方Cookie”）或第三方設置，例如在網站上提供內容或提供廣告或分析服務的人（“第三方Cookie”）。當您訪問我們的網站時以及訪問某些其他網站時，這些第三方可以識別您。</span>
